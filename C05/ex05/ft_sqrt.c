int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	else if (nb == 0 || nb == 1)
		return (1);
	else
	{
		while (i * i <= nb)
		{
			if (i * i == nb)
				return (i);
			if (nb == 2147483647)
				return (0);
			i++;
		}
	}
	return (0);
}
